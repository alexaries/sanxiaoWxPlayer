class TG_ElementEntry {
    //元素块ID
    public id:number;
    // 元素id
    public layerid:number;
    //元素块名字
    public name:string;
    //编辑器类型 1-6
    public edittype:string;
    //当前元素所属层数 1-8
    public layer:number;
    //共存层1: 当前层是否和1层共存,1是共存，-1是不共存
    public layer1:number;
    //共存层2: 当前层是否和2层共存 ,0是共存，-1是不共存
    public layer2:number;
    //共存层3: 当前层是否和3层共存,0是共存，-1是不共存
    public layer3:number;
    //元素图片名称
    public image:string;
    //指定消除
    public target:number;
    // 爆炸后的得分
    public score:number;

    // 逻辑需要参数
    // 对象类型
    public itemType:number;
    // 特效类型
    public effectType:number;
    // 颜色
    public color:number;
    // 扩展效果
    public extendType:number;
    // 扩展参数
    public extendParam:number;
    // 下个阶段
    public nextID:number;
    // 是否可以参与同色块爆炸 0 1
    public canAddMark:number;
    // 是否可以掉落 0 1
    public canFallDown:number;
    // 是否可以交换移动 0 1
    public canSwapMove:number;
    // 是否可以发生爆炸 0 1
    public canExplode:number;
    // 是否可以穿过底块 0 1
    public canFallThrough:number;
    // 是否可以被周围的块爆炸而引爆 0 1
    public canAroundDetonate:number;
    // 是否显示在配置表中（是否在编辑器可见） 0 1
    public editorVisible:number;
    // 引爆方向  如: 1,1,1,1 上 下 左 右 是否可以引爆
    public detonateDirection:string;
    // 引爆方向:顶部是否可以引爆
    // public detonateTop:boolean;
    // 引爆方向:底部是否可以引爆
    // public detonateButtom:boolean;
    // 引爆方向:左侧是否可以引爆
    // public detonateLeft:boolean;
    // 引爆方向:右侧是否可以引爆
    // public detonateRight:boolean;
    // 是否引爆冰层 0 1
    public isDetonateIce:number;
    //是否在收集模式下检测是否存在(收集模式检查是否提前结束)
    // public isAdvanceEnd:boolean;

    // 是否可以使用道具 0 1
    public canUseTools:number;
    // 是否可以与黑洞交换 0 1
    public canSwapWithBlank:number;
    // 是否允许设置掉落 0 1
    public setDrop:number;
    // 可用模式 0 1
    public availableMode:number;
    // 魔法石爆炸方向1上2下3左4右
    public directionExplode:number;
    // 子元素块
    public children:string;
    // 父元素块 如:2042,2002
    public fatherElement:string;
    // 检查类型
    public checkType:number;
    // 棋盘中有某些块时，无需检查数量是否足够 如: 2313,2329,2330
    public specialCheckpoint:string;
    // 是否可以设置主题 0 1
    public isSetMotif:number;

    // setting and getting

}
