class TsList {
    public static size(arr):number {
        return  arr.length;
    }

    public static contains(arr, one):boolean {
        let oneTemp;
        for (let i = 0;i<arr.length;i++) {
            oneTemp = arr[i];
            if (oneTemp == one) {
                return true;
            }
        }
        return false;
    }
}